// Get all NFTs
export const getAllNFTs = (): NFTItem[] => {
  return [...houseArtworkNFTs, ...aiGeneratedNFTs, ...animatedNFTs];
};

// Get NFTs by category
export const getNFTsByCategory = (category: string): NFTItem[] => {
  return getAllNFTs().filter((nft) => nft.category === category);
};

// Get NFTs by rarity
export const getNFTsByRarity = (rarity: string): NFTItem[] => {
  return getAllNFTs().filter((nft) => nft.rarity === rarity);
};

// Get a single NFT by ID
export const getNFTById = (id: string): NFTItem | undefined => {
  return getAllNFTs().find((nft) => nft.id === id);
};
